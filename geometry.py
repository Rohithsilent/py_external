import math

def area_circle(radius):
    return math.pi*radius**2

def circumference(radius):
    return 2*math.pi*radius

def area_rectangle(l,b):
    return l*b

def perimeter_rectangle(l,b):
    return 2*(l+b)

def area_square(side):
    return side**2

def perimeter_square(side):
    return 4*side